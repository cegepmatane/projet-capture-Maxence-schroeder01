package modele;

public class meteo {

	protected int id;
	protected String jour;
	protected String mois;
	protected String annee;
	protected String temps;
	protected String vent;
	protected String temperature;
	protected String humidite;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getjour() {
		return jour;
	}
	public void setjour(String jour) {
		this.jour = jour;
	}
	public String getmois() {
		return mois;
	}
	public void setmois(String jour) {
		this.mois = jour;
	}
	public String getannee() {
		return annee;
	}
	public void setannee(String jour) {
		this.annee = jour;
	}
	public String gettemps() {
		return temps;
	}
	public void settemps(String rayon) {
		this.temps = rayon;
	}
	public String getvent() {
		return vent;
	}
	public void setvent(String jour) {
		this.vent = jour;
	}
	public String getTemperature() {
		return temperature;
	}
	public void setTemperature(String temperature) {
		this.temperature = temperature;
	}
	public String gethumidite() {
		return humidite;
	}
	public void sethumidite(String temperature) {
		this.humidite = temperature;
	}

	
}
